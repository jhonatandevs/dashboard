export async function generateStaticParams() {
  const staticPokemons = Array.from({ length: 151 }).map((v, i) => `${i + 1}`);
  return staticPokemons.map(id => ({
    id: id
  }))
}
export async function generateMetadata({ params }: Props): Promise<Metadata> {
  const pokemon = await getPokemon(params.id);

  return {
    title: pokemon.name,
    description: `Esta es el detalle de ${pokemon.name}`,
    icons: {
      icon: '/next.svg',
    }
  }
}
const getPokemon = async (id = '1'): Promise<PokemonDetailResponse> => {
  try {
    const data: PokemonDetailResponse = await fetch(`https://pokeapi.co/api/v2/pokemon/${id}`, {
      cache: 'force-cache', //TODO: CAMBIAR ESTO EN FUTURO,
      next: {
        revalidate: 60 * 60 * 30 * 6
      }
    })
      .then(res => res.json());
    return data;
  }
  catch (error) {
    notFound()
  }
}
